func MakeSFeature(coords [][]float64, fq FQNID, fc *geojson.FeatureCollection) {
   f := geojson.NewLineStringFeature(coords)
   f.SetProperty("object_id", fq.ObjectId)
   f.SetProperty("type", GetFQType(fq))
   f.SetProperty("dyea_vz", fq.FQNID)
   fc.AddFeature(f)
} // }}}

// func GetFQType {{{
//
// Underground FQNIDs
// FIB:BUR::XXXXXX || FIB:TAIL::XXXXXX || OSP:UGSPAN::XXXXXX
// Aerial FQNIDs
// FIB:AER::XXXXXX || OSP:AERSPAN::XXXXXX
//
// Returns a normalized verison of the permit type
func GetFQType(fq FQNID) string {
   if fq.FQNID == "" {
      return ""
   }

   var fqType string

   // fqnid[0] == FIB, fqnid[1] == XXX:, fqnid[2] == XXXXXX
   fqnid := strings.SplitAfter(fq.FQNID, ":")
   fqn := strings.ToUpper(fqnid[1])

   f := strings.Split(fqn, ":")
   fqType = f[0]

   if fqType == "AER" || fqType == "AERSPAN" {
      return "ae"
   }
   return "ug"
} // }}}
